const assert = require("node:assert/strict");
const fs = require("node:fs");
const path = require("node:path");
const ROOT = path.join(__dirname, "..");
const contract = JSON.parse(fs.readFileSync(path.join(ROOT, "fixtures", "security-arm-v0.1-contract.json"), "utf8"));
const document = fs.readFileSync(path.join(ROOT, "SECURITY_ARM_CONTRACT.md"), "utf8");
const discovery = JSON.parse(fs.readFileSync(path.join(ROOT, "fixtures", "discovery-v0.1-contract.json"), "utf8"));

assert.equal(contract.contractType, "ExternalSecurityBoundary");
assert.equal(contract.contractVersion, "0.1");
assert.equal(contract.bundledMalwareScanning, false);
assert.deepEqual(contract.bundledProviders, []);
assert.equal(contract.windowsDefenderIntegration, false);
for (const field of ["actorId", "timestamp", "reason", "fileVersionId", "discoveryCaseId"]) assert.equal(contract.requiredAcknowledgment[field], true, `Acknowledgment field missing: ${field}`);
for (const control of ["copy_original_without_moving", "sha256_exact_identity", "block_changed_or_missing_staged_bytes", "append_only_acknowledgment_event"]) assert(contract.enforcedControls.includes(control), `External security control missing: ${control}`);
for (const claim of ["malware_free", "clean", "safe", "threat_free"]) assert(contract.forbiddenClaims.includes(claim), `Forbidden security claim missing: ${claim}`);
for (const forbidden of ["approveIntake", "writeCore", "deleteOriginalFile", "rewriteReceiptHistory"]) assert(contract.forbiddenAuthority.includes(forbidden), `Security authority prohibition missing: ${forbidden}`);
assert(document.includes("Project State is not antivirus software"));
assert(document.includes("does not scan files or claim they are safe"));
assert(document.includes("Only add files you trust and have already checked"));
assert(discovery.governingSequence.includes("external_security_acknowledgment"));
console.log("External Security Boundary Check");
console.log(JSON.stringify({ version: contract.contractVersion, bundledMalwareScanning: false, bundledProviders: 0, acknowledgmentFields: Object.keys(contract.requiredAcknowledgment).length, enforcedControls: contract.enforcedControls.length, forbiddenClaims: contract.forbiddenClaims.length }, null, 2));
console.log("External security boundary: ok");
